  blocks[`${food.x}-${food.y}`].classList.remove('food');
  snake.forEach((segment) => {
    blocks[`${segment.x}-${segment.y}`].classList.remove('fill');
  });

  modal.style.display = 'none';

  snake = [{ x: 1, y: 3 }];
  food = {
    x: Math.floor(Math.random() * rows),
    y: Math.floor(Math.random() * cols)
  };
