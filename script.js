addEventListener('keydown', (event) => {
  if (event.key == 'ArrowRight') direction = 'right';
  else if (event.key == 'ArrowLeft') direction = 'left';
  else if (event.key == 'ArrowUp') direction = 'up';
  else if (event.key == 'ArrowDown') direction = 'down';
});
